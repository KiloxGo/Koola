package cn.peyriat.koola.util

class GameUtils {
    class vector3 {
        var x: Float = 0f
        var y: Float = 0f
        var z: Float = 0f

        constructor(x: Float, y: Float, z: Float) {
            this.x = x
            this.y = y
            this.z = z
        }

        constructor() {
            this.x = 0f
            this.y = 0f
            this.z = 0f
        }
    }

}